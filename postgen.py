# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ –ü–æ—Å—Ç—ã –∏–∑ PDF —á–µ—Ä–µ–∑ GPT
def generate_from_pdf():
    raw_text = extract_text()[:3000]
    prompt = (
        f"–í–æ—Ç –≤—ã–¥–µ—Ä–∂–∫–∞ –∏–∑ —Ç–µ–∫—Å—Ç–∞ –æ –º–∏–∫—Ä–æ–¥–æ–∑–∏–Ω–≥–µ:\n\n{raw_text}\n\n"
        "–ù–∞ –æ—Å–Ω–æ–≤–µ —ç—Ç–æ–≥–æ –Ω–∞–ø–∏—à–∏ –≤–¥–æ—Ö–Ω–æ–≤–ª—è—é—â–∏–π Telegram-–ø–æ—Å—Ç –ø—Ä–æ –≥—Ä–∏–±—ã. "
        "–î–æ–±–∞–≤—å –∑–∞–≥–æ–ª–æ–≤–æ–∫, —ç–º–æ–¥–∑–∏, —Ö–µ—à—Ç–µ–≥–∏. –ü–æ—Å—Ç –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å –∫–æ—Ä–æ—Ç–∫–∏–º, –ø–æ—ç—Ç–∏—á–Ω—ã–º, –º–∏—Å—Ç–∏—á–µ—Å–∫–∏–º, –Ω–æ –ø–æ–Ω—è—Ç–Ω—ã–º."
    )

    response = client.chat.completions.create(
        model="gpt-4-turbo",
        messages=[{"role": "user", "content": prompt}],
        max_tokens=400,
        temperature=0.8,
    )

    content = response.choices[0].message.content.strip()
    title = content.split("\n")[0]
    body = "\n".join(content.split("\n")[1:])

    return {
        "title": title,
        "content": body,
        "created_at": datetime.now().isoformat()
    }
